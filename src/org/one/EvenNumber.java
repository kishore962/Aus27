package org.one;
import java.util.*;
public class EvenNumber {
	public static void main(String[] args) {
		Scanner k= new Scanner(System.in);
		System.out.println("enter a number");
		int a=k.nextInt();
				
		if (a%2==0) {
		
			System.out.println(a+"is a even number");
			
		}
		else {
			System.out.println(a+"is a odd number");
		}
		
		
		
		}
	}


